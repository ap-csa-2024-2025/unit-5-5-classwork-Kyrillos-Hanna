public class Dog
{
    private String breed;
    private String name;
    private double size;
    private String furColor;

    public String getBreed() {
        return breed;
    } 

    public String getName() {
        return name;
    }
    
    public double getSize() {
        return size;
    }

    public String getFurColor() {
        return furColor;
    }

    public void setBreed(String bread) {
        breed = bread;
    }

    public void setName(String word) {
        name = word;
    }

    public void setSize(double num) {
        size = num;
    }

    public void setFurColor(String fur) {
        furColor = fur;
    }
}
